def square(number):
    """
    This function returns square of a given number
    """
    return number ** 2

def double(number):
    """
    This function returns twice the value of given number
    """
    return number * 2

def add(a, b):
    """
    This function returns the sum of given two numbers
    """
    return a + b

def multiply(a, b):
    """
    This function returns the product of given two numbers
    """
    return a * b

def divide(a, b):
    """
    This function returns the result of division of two numbers
    """
    return a / b